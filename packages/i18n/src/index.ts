export function formatMoney(locale: Locale, amountVnd: number, currency: 'VND' | 'USD' = 'VND') {
  const amount = currency === 'USD' ? amountVnd / 25_400 : amountVnd;
  return new Intl.NumberFormat(intlLocales[locale], {
    style: 'currency',
    currency,
    maximumFractionDigits: currency === 'VND' ? 0 : 2,
  }).format(amount);
}

export function formatDate(locale: Locale, value: Date | string) {
  return new Intl.DateTimeFormat(intlLocales[locale], {
    dateStyle: 'medium',
  }).format(typeof value === 'string' ? new Date(value) : value);
}

export function alternateLocale(locale: Locale): Locale {
  return locale === 'vi' ? 'en' : 'vi';
}

